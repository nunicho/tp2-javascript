/* 13- Realiza un script que pida un texto y lo muestre en mayúsculas.

*/

let texto = prompt("Ingrese un texto que desee convertir a mayúsculas");

texto = texto.toUpperCase()

document.write(texto)

/*
El toUpperCase() método devuelve el valor convertido en mayúsculas de la cadena que realiza la llamada.

https://developer.mozilla.org/es/docs/Web/JavaScript/Reference/Global_Objects/String/toUpperCase
*/